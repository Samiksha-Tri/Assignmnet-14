"""Write a Python script to print distinct elements along with their frequencies of
occurrence in the list.
"""

l1=[1,1,2,2,2,5,8,9,9,9,9]
print("1 :",l1.count(1),"times")
print("2 :",l1.count(2),"times")
print("5:",l1.count(5),"times")
print("8 :",l1.count(8),"times")
print("9 :",l1.count(9),"times")